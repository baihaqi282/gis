<p>Peta Titik Point dan Polygon Area Yang Ada Di Wilayah Tabalong.</p>

<p>Peta Online yang menampilkan Data Base Titik Point dan Batas Wilayah Administrasi antar Desa/Kelurahan, Kecamatan serta dengan Kabupaten dan Provinsi lain. 
Data base Titik Point akan di update secara bertahap dan mengikuti perkembangan perubahan yang ada. <br>
Batas Kabupaten dan Provinsi sesuai dengan : <br>
1. Permendagri No. 65 Tahun 2013 tentang Batas antara Kabupaten HSU dengan Kabupaten Tabalong. <br>
2. Permendagri No. 66 Tahun 2014 tentang Batas antara Kabupaten Tabalong dengan Kabupaten Balangan. <br>
3. Permendagri No. 46 Tahun 2016 tentang Batas antara Kabupaten Tabalong dengan Kabupaten Paser Kaltim. <br>
4. Permendagri No. 40 Tahun 2018 tentang Batas antara Kabupaten Tabalong dengan Kabupaten Bartim Kalteng.</p>


